--练习2：请根据年度月份收入表 AMT623，查找每个月的环比、同比
--展示字段：日期、今年月收入、上个月收入、环比、去年月收入、同比
--如果收入为空则展示为0
--环比公式：（当月的收入-上个月的收入）/上个月的收入
--同比公式：（当年月的收入-去年月的收入）/去年月的收入

SELECT T2.YYYYMM,
       T2.AMT,
       NVL(T2.LAST_MONTH_AMT, 0),
       ROUND((T2.AMT - NVL(T2.LAST_MONTH_AMT, 0)) / NVL(T2.LAST_MONTH_AMT, T2.AMT) * 100, 2) || '%' AS MoM,
       NVL(T2.LAST_YEAR_MONTH_AMT, 0),
       ROUND((T2.AMT - NVL(T2.LAST_YEAR_MONTH_AMT, 0)) / NVL(T2.LAST_YEAR_MONTH_AMT, T2.AMT) * 100, 2) || '%' MoM_Over_YEAR
FROM(
SELECT T.*,
       LAG(T.AMT,1,NULL)OVER(ORDER BY YYYYMM) AS LAST_MONTH_AMT,
       LAG(T.AMT,1,NULL)OVER(PARTITION BY SUBSTR(YYYYMM,5) ORDER BY SUBSTR(YYYYMM,1,4)) AS LAST_YEAR_MONTH_AMT
FROM AMT623 T) T2







