--练习1：查询员工工号、姓名、工资、
--工资占比全公司工资支出的百分比保留两个小数点。
SELECT EMPNO,
       ENAME,
       SAL,
       ROUND(SAL/SUM(SAL) OVER() *100,2) || '%' AS SAL_PERCENT
FROM EMP;


